-- src/main/resources/schema.sql

-- Create 'roles' table if it does not already exist
CREATE TABLE IF NOT EXISTS roles (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- H2/PostgreSQL compatible auto-increment
    name VARCHAR(20) NOT NULL UNIQUE                       -- e.g., 'ROLE_USER', 'ROLE_ADMIN'
);

-- Create 'users' table if it does not already exist
CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- H2/PostgreSQL compatible auto-increment
    username VARCHAR(50) NOT NULL UNIQUE,
    email VARCHAR(100) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,                       -- Stores the hashed password
    first_name VARCHAR(50),
    last_name VARCHAR(50),
    phone_number VARCHAR(20),                             -- Mapped from 'phoneNumber'
    shipping_address VARCHAR(255),                        -- Mapped from 'shippingAddress'
    enabled BOOLEAN DEFAULT TRUE NOT NULL,                -- Mapped from 'enabled'
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL, -- Mapped from 'createdAt'
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL  -- Mapped from 'updatedAt'. R2DBC often handles updates in app.
);

-- Create 'user_roles' join table if it does not already exist
CREATE TABLE IF NOT EXISTS user_roles (
    user_id BIGINT NOT NULL,
    role_id BIGINT NOT NULL,
    PRIMARY KEY (user_id, role_id),
    FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    FOREIGN KEY (role_id) REFERENCES roles(id) ON DELETE CASCADE
);